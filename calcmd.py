import math
import kandinsky

def draw_line(x1, y1, x2, y2, width, color):
    if x2 < x1:
        x1, y1, x2, y2, = x2, y2, x1, y1
    xo = x2-x1
    yo = y2-y1
    y_error = yo/xo if xo != 0 else yo
    half_width = width/2

    if xo == 0:
        if yo > 0:
            kandinsky.fill_rect(x1-int(half_width), y1-half_width, width, yo+width, color)
        else:
            kandinsky.fill_rect(x1-int(half_width), y2-half_width, width, -yo+width, color)
        return
    
    y_width = abs(math.ceil(y_error)) if abs(y_error) > width else width

    for i in range(xo+1):
        y = math.ceil(y1+i*y_error-half_width)

        ny_width = y_width
        if i == xo and y2 > y1:
            ny_width = width
        if i == 0 and y2 < y1 and y+y_width > y1:
            ny_width = width

        kandinsky.fill_rect(int(x1+i-half_width), y, width, ny_width, color)

